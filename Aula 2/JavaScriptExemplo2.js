let a = 10;
let b = 4;

console.log("Valores iniciais:");
console.log("a = " + a);
console.log("b = " + b);

// Atribuições aritméticas
a += 5;
console.log("a += 5: " + a);

b -= 2;
console.log("b -= 2: " + b);

a *= 2;
console.log("a *= 2: " + a);

b /= 2;
console.log("b /= 2: " + b);

a %= 7;
console.log("a %= 7: " + a);

a **=3;
console.log("a **= 3: " + a);

//Incremento e Decremento
let x = 10;
console.log("\nIncremento e Decremento:");
console.log("x = " + x);

x++; //pós-incremento
console.log("x++: " + x);

x--; //pós-decremento
console.log("x--: "+ x);

++x; //pré-incremento
console.log("++x: "+ x);

--x; //pré-decremento
console.log("--x: "+ x);

//Negação númerica
let y = 7;
console.log("\nNegação númerica:");
console.log("y = " + y);
console.log("-y: " + (-y));